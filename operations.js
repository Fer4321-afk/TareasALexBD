/* Función suma */
function suma(a, b) {
  return a + b;
}

/* Función resta */
function resta(a, b) {
  return a - b;
}

function multiplicacion(a, b) {
  return a * b;
}

/* Función división */
function division(a, b) {
  return a / b;
}

function Resto(a, b) {
  return a % b;
}

/* Exportación de funciones */
module.exports = {
  suma,
  resta,
  multiplicacion,
  division,
  Resto,
};
